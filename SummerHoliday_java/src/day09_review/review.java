package day09_review;

public class review {
/* relational operators : (turns true if true...)
 * >   greater	
 * <   smaller
 * >=  greater than or equals
 * <=  smaller than or equals
 * ==  equals to
 * !=  not equals
 */    
	
	
/* 	logical operators
 *  &&  and
 *  ||  or
 */
	public static void main(String[] args) {
		
		System.out.println('a' > 'A');// true
		System.out.println((int)'a'+(int)'A'); //162
		System.out.println((int)'a'+""+(int)'A'); //9765
		
		System.out.println(true != false && false == false);
	}						//true and true
	                        // true
}
